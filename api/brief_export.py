def build_docx(result):
    from docx import Document
    from docx.shared import Pt, RGBColor

    doc = Document()
    doc.add_heading("WaitCost — Decision Brief", level=0)

    q = result.get("plan", {}).get("question") or result.get("question", "")
    meta = doc.add_paragraph()
    meta.add_run("Question: ").bold = True
    meta.add_run(str(q))

    doc.add_heading("Headline", level=1)
    h = doc.add_paragraph(_headline(result))
    h.runs[0].bold = True

    chart = chart_table(result)
    if chart:
        doc.add_heading(f"Recommended chart — {chart['title']}", level=1)
        t = doc.add_table(rows=1, cols=2)
        t.style = "Light Grid Accent 1"
        hdr = t.rows[0].cells
        hdr[0].text, hdr[1].text = "Category", chart["y_label"] or "Value"
        for lab, val in zip(chart["labels"], chart["values"]):
            row = t.add_row().cells
            row[0].text, row[1].text = lab, f"{val:,.1f}"

    doc.add_heading("Full brief", level=1)
    for block in _strip_md(result.get("brief_markdown", "")).split("\n"):
        if block.strip():
            doc.add_paragraph(block)

    doc.add_heading("Sources", level=1)
    doc.add_paragraph(SOURCES)
    disc = doc.add_paragraph(DISCLAIMER)
    disc.runs[0].italic = True
    foot = doc.add_paragraph(FOOTER)
    foot.runs[0].font.size = Pt(8)
    foot.runs[0].font.color.rgb = RGBColor(0x80, 0x80, 0x80)

    buf = io.BytesIO()
    doc.save(buf)
    return buf.getvalue()


def build_pdf(result):
    from reportlab.lib.pagesizes import letter
    from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
    from reportlab.lib.units import inch
    from reportlab.lib import colors
    from reportlab.platypus import (SimpleDocTemplate, Paragraph, Spacer, Table,
                                    TableStyle)
    from reportlab.graphics.shapes import Drawing
    from reportlab.graphics.charts.barcharts import VerticalBarChart

    styles = getSampleStyleSheet()
    small = ParagraphStyle("small", parent=styles["Normal"], fontSize=8,
                           textColor=colors.grey)
    buf = io.BytesIO()
    docp = SimpleDocTemplate(buf, pagesize=letter, topMargin=0.7 * inch,
                             bottomMargin=0.7 * inch)
    flow = [Paragraph("WaitCost — Decision Brief", styles["Title"])]

    q = result.get("plan", {}).get("question") or result.get("question", "")
    flow.append(Paragraph(f"<b>Question:</b> {q}", styles["Normal"]))
    flow.append(Spacer(1, 8))
    flow.append(Paragraph("<b>Headline</b>", styles["Heading2"]))
    flow.append(Paragraph(_headline(result), styles["Normal"]))
    flow.append(Spacer(1, 8))

    chart = chart_table(result)
    if chart:
        flow.append(Paragraph(f"<b>Recommended chart — {chart['title']}</b>", styles["Heading2"]))
        d = Drawing(440, 200)
        bc = VerticalBarChart()
        bc.x, bc.y, bc.width, bc.height = 30, 25, 390, 150
        bc.data = [chart["values"]]
        bc.categoryAxis.categoryNames = chart["labels"]
        bc.categoryAxis.labels.angle = 30
        bc.categoryAxis.labels.dy = -6
        bc.barWidth = 6
        bc.valueAxis.valueMin = min(0, min(chart["values"]))
        bc.bars[0].fillColor = colors.HexColor("#1a73e8")
        d.add(bc)
        flow.append(d)
        flow.append(Spacer(1, 8))

    flow.append(Paragraph("<b>Full brief</b>", styles["Heading2"]))
    for block in _strip_md(result.get("brief_markdown", "")).split("\n"):
        if block.strip():
            flow.append(Paragraph(block.replace("&", "&amp;"), styles["Normal"]))
            flow.append(Spacer(1, 2))

    flow.append(Spacer(1, 10))
    flow.append(Paragraph("<b>Sources</b>", styles["Heading2"]))
    flow.append(Paragraph(SOURCES, styles["Normal"]))
    flow.append(Spacer(1, 6))
    flow.append(Paragraph("<i>" + DISCLAIMER + "</i>", styles["Normal"]))
    flow.append(Spacer(1, 6))
    flow.append(Paragraph(FOOTER, small))

    docp.build(flow)
    return buf.getvalue()
