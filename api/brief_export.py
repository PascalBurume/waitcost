"""Feature ② — export the decision brief to PDF / Word (both fully offline).

Renders the SAME figures the engine produced (no recomputation): the headline
number + range, the narrated brief, the recommended chart, the sources, and the
standing disclaimer. PDF uses reportlab; Word uses python-docx. Returned as raw
bytes for a file download.
"""
import io
import re

FOOTER = ("Generated by WaitCost · HUD 2024 PIT · Census ACS 2024 (API) · figures are ranges.")
SOURCES = ("HUD 2024 PIT CoC Population & Subpopulation · US Census ACS 2024 1-yr (API) · "
           "HUD System Performance Measures FY2023 · Economic Roundtable (2024$).")
DISCLAIMER = ("This tool informs a budget-timing tradeoff. It does not decide allocations "
              "or forecast individuals. All figures are ranges, not predictions.")


def _strip_md(text):
    """Light markdown -> plain text (bold/headers/list markers) for body prose."""
    text = re.sub(r"\*\*(.+?)\*\*", r"\1", text)
    text = re.sub(r"^#{1,6}\s*", "", text, flags=re.MULTILINE)
    text = re.sub(r"^>\s*", "", text, flags=re.MULTILINE)
    text = re.sub(r"^[-*]\s+", "• ", text, flags=re.MULTILINE)
    return text


def _headline(result):
    """The one-line headline answer (markdown stripped), or a sensible default."""
    d = result.get("direct_answer")
    return _strip_md(d).strip() if d else "See decision brief below."


def chart_table(result):
    """Extract a compact (labels, values, value_label) view of the recommended
    chart's first numeric series — used to render a small chart/table in the export."""
    try:
        from api import payloads as P
        name = result.get("recommended_chart") or "cost_trajectory"
        coc = _coc(result)
        budget = float(result.get("plan", {}).get("budget_musd", 50.0))
        delay = int(result.get("plan", {}).get("delay_years", 3) or 0)
        spec = P.chart_payload(name, coc=coc, budget=budget, delay=max(delay, 1))
        s = (spec.get("series") or [{}])[0]
        x, y = s.get("x"), s.get("y")
        # numeric y -> labels=x; else numeric x (horizontal/tornado) -> labels=y
        if isinstance(y, list) and y and all(isinstance(v, (int, float)) for v in y):
            labels, values = [str(v) for v in x], [float(v) for v in y]
        elif isinstance(x, list) and x and all(isinstance(v, (int, float)) for v in x):
            labels, values = [str(v) for v in y], [float(v) for v in x]
        else:
            return None
        return {"title": spec.get("title", name), "y_label": spec.get("y_label", ""),
                "labels": labels[:12], "values": values[:12]}
    except Exception:
        return None


def _coc(result):
    runs = result.get("runs") or {}
    # CoC isn't on the top-level result; recover it from the plan question / default.
    m = re.search(r"([A-Z]{2}-\d{3})", str(result.get("plan", {}).get("question", "")))
    return m.group(1) if m else "CA-600"


